	def insert_MediaLink_Path ( edit, view ):

		filePath_Regions = []

		settings                 = sublime.load_settings ( "SlideNav.sublime-settings" )
		mediaPath_TabAmount      = settings.get ( "mediapath_tab_amount", "" )
		mediaLink_EndCap         = settings.get ( "medialink_endcap", "" )
